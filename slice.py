lst = ['e', 'm', 'r', 'e', '.', 'm', 'e']

print(lst[1::])
print(lst[0])
print(lst[-1])
print(lst[::-1])    # all items in the list, reversed
print(lst[1::-1])   # the first two items, reversed
print(lst[:-3:-1]) # the last two items, reversed
print(lst[-3::-1])  # everything except the last two items, reversed




